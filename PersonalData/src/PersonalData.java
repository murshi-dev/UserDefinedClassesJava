
public class PersonalData {
	private String name;
	private String address;
	private int age;
	private int phone;
	//Constructor
	PersonalData()
	{
		name="";
		address="";
		age=0;
		phone=0;
	}
	//setter methods
	 public void setName(String n) {
		 name=n;		 
	 }
	 public void setAddress(String a) {
		 address=a;
	 }
	 public void setAge(int a) {
		 age=a;
	 }
	 public void setPhone(int p) {
		 phone=p;
	 }
	 //getter methods
	 public String getName()
	 {
		 return name;
	 }
	 public String getAddress()
	 {
		 return address;
	 }
	 public int getAge()
	 {
		 return age;
	 }
	 public int getPhone()
	 {
		 return phone;
	 }
	 
	 
	 
	 
	 
	 
	 
	 
	 
	 

}
